package br.com.supportcomm.virtualgoodscore.billing;

/**
 * Status do crédito do cliente para uma operação de cobrança.
 */
public enum CreditStatus
{
    /**
     * Cliente possui crédito suficiente para a cobrança.
     */
    ENOUGH_CREDIT, 
    
    /**
     * Cliente não possui crédito suficiente para a cobrança.
     */
    INSUFFICIENT_CREDIT, 
    
    /**
     * Falha na consulta de crédito.
     */
    BILLING_SYSTEM_FAILURE;
}
